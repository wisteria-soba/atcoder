ll modinv(ll a, ll m) {
    ll b = m, u = 1, v = 0;
    while (b) {
        ll t = a / b;
        a -= t * b; swap(a, b);
        u -= t * v; swap(u, v);
    }
    u %= m;
    if (u < 0) u += m;
    return u;
}

const int MOD = 1000000007;
const int MAX = 510000;

long long fac[MAX], finv[MAX], inv[MAX];

// テーブルを作る前処理
void COMinit() {
    fac[0] = fac[1] = 1;
    finv[0] = finv[1] = 1;
    inv[1] = 1;
    for (int i = 2; i < MAX; i++){
        fac[i] = fac[i - 1] * i % MOD;
        inv[i] = MOD - inv[MOD%i] * (MOD / i) % MOD;
        finv[i] = finv[i - 1] * inv[i] % MOD;
    }
}

// 二項係数計算
long long COM(int n, int k){
    if (n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return fac[n] * (finv[k] * finv[n - k] % MOD) % MOD;
}

ll nmod(ll a) {
    return ((a % MOD < 0) ? a % MOD + MOD : a % MOD);
}

int main(void)
{
    int n,k;
    cin >> n >> k;
    vector<int> a(n);
    REP(i,n){
        cin >> a[i];
    }

    ll ans = 0;
    sort(all(a));
    COMinit();

    for (int i = k - 1; i < n;i++)
    {
        ans = nmod(ans + nmod(a[i] * COM(i, k - 1)));
    }

    for (int i = 0; i <= n - k;i++){
        ans = nmod(ans - nmod(a[i] * COM(n-i-1,k-1)));
    }

    cout << ans << endl;

    return 0;
}